    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try{
            String nomeCliente = campoNome.getText();
            String cpfCliente = campoCpf.getText();
            
            if(!nomeCliente.equals("") && !cpfCliente.equals("")){
                
                Cliente clienteCadastrado = new Cliente(nomeCliente, cpfCliente);
                ListaClientes.adicionarCliente(clienteCadastrado);
                
                this.listarClientes();
            }else{
                throw new CampoVazio("Existe algum campo de texto vazio!");
            }
        }catch(CampoVazio erro){
            erro.printStackTrace();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

