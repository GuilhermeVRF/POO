    private void listarClientes(){
        ArrayList nomeColunas = new ArrayList<>();
        nomeColunas.add("NOME");
        nomeColunas.add("CPF");
        
        ArrayList<Object[]> dadosTabela = new ArrayList<>();
        
        for(Cliente cliente: ListaClientes.getListaClientes()){
            ArrayList<Object> dadosCliente = new ArrayList<>();
            dadosCliente.add(cliente.getNome());
            dadosCliente.add(cliente.getCpf());
            
            dadosTabela.add(dadosCliente.toArray());
        }
        
        DefaultTableModel estruturaTabela = new DefaultTableModel(dadosTabela.toArray(new Object[dadosTabela.size()][]), nomeColunas.toArray());
        this.tabelaClientes.setModel(estruturaTabela);
        
    }    
